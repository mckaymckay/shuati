    x = 0
    for p in range(0, length):
        if nums[p] == 0:
            nums[p], nums[x] = nums[x], nums[p]
            x += 1
    for p in range(x, length):
        if nums[p] == 1:
            nums[p], nums[x] = nums[x], nums[p]
            x += 1
    return nums


