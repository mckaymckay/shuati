    low = 0
    high = len(my_list) - 1
    while low <= high:
        mid = (low + high) // 2
        if item == my_list[mid]:
            return mid
        elif item < my_list[mid]:
            high = mid - 1
        else:
            low = mid + 1
        print(low, high, mid)

    return low


